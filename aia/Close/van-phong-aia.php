                                        self.appendTo(document.body)
                                    }
                                });
                        	} else {
                        		$('[data-target="#' + btnId +'"]').attr('data-target','#'+modalId[idx]);
                        	}
                        } else if(self.data("modalTagMoveTo")) {
            	if( self.data("modalTagMoveTo") == "body" ){
            		self.detach().appendTo(document.body)
            	}
                        }
                    });

                    $('.aia-modal').each(function(){
            
                        var self = $(this);
                        var target = $(this).attr('data-target'),
                            container = $(this).find('.modal-container');
            
                        var wcmmode = '';
            
                        if(target && container && typeof target!='undefined' && target.length>0 && container.length>0) {
                        	var idx = -1,
                        		btnId = self.attr('id'),
                                param = { parentUrl: "/content/vn/vi/gioi-thieu-ve-aia" };
            
                            if(!Array.prototype.indexOf) {
                                for(var i=0;i<list.length;i++) {
                                    if(list[i]==target) {
            				idx=i;
                                        break;
                                    }
                                }
                            } else {
            		idx = list.indexOf(target);
                            }
            
                            if(wcmmode!='') {
            		param.wcmmode = wcmmode;
                            }
            
                        	if (idx<0) {
                                if(btnId.length>0) {
                                    list.push(target);
                                    modalId.push(btnId);
                                }
                                $.ajax({
                                    url:target,
                                    method:'GET',
                                    data: param
                             }).success(function(data) {
                                    container.html(data);
                                    if( self.data("modalTagMoveTo") == "body" ){
                                        self.appendTo(document.body)
